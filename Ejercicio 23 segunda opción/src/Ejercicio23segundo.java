/*Imprime los numeros del 0 al 100, controlando las filas y las 
 * columnas
 */

public class Ejercicio23segundo {
	public static void main(String[] args) {
		int fin=0;
		
		for (int i = 0; i <= 100; i++) {

		System.out.print(i+" ");
		fin++;
		
		if (fin>9) {
			System.out.println();
			fin=0;
		
	}
		
	
		
		
			
		}
		
	}

}
